void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if (((*htim).Instance==TIM3) && state == current_temp){
	
	  BSP_LED_Toggle(LED4); //RED LED ON

		// Update fan speed
		fan_speed = (temp-set_temp)*30;// to gradually increase fan speed, we multiply the difference between temp and setpoint by 30 percent
		if (fan_speed>100) fan_speed = 100; //fan speed can never be over 100 percent
		else if (fan_speed<0) fan_speed = 0;// fan speed can never be under 0 percent
	
		htim4.Instance->CCR1 = (uint32_t)fan_speed;// PWM duty cycle set by fan speed percentage
	}
}


void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
 switch (GPIO_Pin) {
 case GPIO_PIN_0: //SELECT button
	 if (state==current_temp) state=setpoint;// pressing select, displays setpoint when displaying current temp
	 else if(state==setpoint) state=current_temp;// pressing select, displays current temp when displaying setpoint
 break;
 
 case GPIO_PIN_3: //up button
	 if (state==setpoint)state=inc_point;// increases setpoint when displaying setpoint
 break;
 case GPIO_PIN_5: //down button
	 if (state==setpoint)state=dec_point;//decreases setpoint when displaying setpoint
 break;
 
 default:
 break;
 }
}

