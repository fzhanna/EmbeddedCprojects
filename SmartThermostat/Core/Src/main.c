/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
	
	BSP_LCD_GLASS_Init();
	BSP_LED_Init(LED4);
		//sprintf(lcd_buffer,"%s","hello");
//BSP_LCD_GLASS_DisplayString((uint8_t*)"HELLO");
	
	HAL_TIM_PWM_Start(&htim4,TIM_CHANNEL_1);
	if (HAL_TIM_Base_Start_IT(&htim3)!=HAL_OK)
		Error_Handler();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
//switch used for FSM cases 
			switch(state){
		
		case current_temp:// displays current temp, gets room temp using LM35 output voltage to a number using A/D converter in the MCU then converted to Celsius value
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		temp= (float)HAL_ADC_GetValue(&hadc1)*0.026855468; //3.3/(4096*3*0.01) - Celsius conversion

		sprintf(lcd_buffer, "%f", temp);
		BSP_LCD_GLASS_Clear(); 
		BSP_LCD_GLASS_DisplayString((uint8_t *)lcd_buffer);	
		
		
			break;
		case setpoint://views temperature setpoint by user
			sprintf(lcd_buffer, "%f", set_temp);
			BSP_LCD_GLASS_DisplayString((uint8_t *)lcd_buffer);
			
			break;
		
		
		case inc_point://increments setpoint by .5
			set_temp+=0.5;
			state=setpoint;//goes back to setpoint
		
			break;
		case dec_point://decrements setpoint by .5
			set_temp-=0.5;
			state=setpoint;//goes back to setpoint
			break;
		
	}
		
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

